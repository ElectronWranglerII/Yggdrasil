;CONVERTS THE HEX CHARACTER IN AL TO AN INTEGER
;ON ENTRY:
;	AL = VALUE
;ON RETURN:
;	C = 0 IF CHARACTER IS HEX
;			AL = INTEGER VALUE
;	C = 1 IF CHARACTER IS NOT HEX
;			AL = 0x00
CHARHEXTOINT:
	;CHECK FOR LESS THAN "0"
	SUB		AL, 0x30
	JC		CHARHEXTOINTC
	;CHECK FOR GREATER THAN "9"
	CMP		AL, 0x0A
	JNC		CHARHEXTOINTB
CHARHEXTOINTA:
	CLC
	RET
CHARHEXTOINTB:
	;SUBTRACT 0x07
	SUB		AL, 0x07
	;CHECK FOR LESS THAN "A"
	CMP		AL, 0x0A
	JC		CHARHEXTOINTC
	;CHECK FOR GREATER THAN "F"
	CMP		AL, 0x10
	JC		CHARHEXTOINTA
	;CONVERT TO UPPERCASE
	SUB		AL, 0x20
	JC		CHARHEXTOINTC
	;CHECK FOR LESS THAN "A"
	CMP		AL, 0x0A
	JC		CHARHEXTOINTC
	;CHECK FOR GREATER THAN "F"
	CMP		AL, 0x10
	JC		CHARHEXTOINTA
CHARHEXTOINTC:
	XOR		AL, AL
	STC
	RET


;RETURNS TRUE IF VALUE IS AN ARITHMETIC OPERATOR
;ON ENTRY:
;	AL = VALUE
;ON RETURN:
;	AL = VALUE ON ENTRY
;	C = 0 IF VALUE IS NOT AN ARITHMETIC OPERATOR
;	C = 1 IF VALUE IS AN ARITHMETIC OPERATOR
CHARISARITH:
	;CHECK FOR FACTORAL "!"
	CMP		AL, 0x21
	JZ		CHARISARITHA
	;CHECK FOR MODULO "%"
	CMP		AL, 0x25
	JZ		CHARISARITHA
	;CHECK FOR MULTIPLICATION "*"
	CMP		AL, 0x2A
	JZ		CHARISARITHA
	;CHECK FOR ADDITION "+"
	CMP		AL, 0x2B
	JZ		CHARISARITHA
	;CHECK FOR SUBTRACTION "-"
	CMP		AL, 0x2D
	JZ		CHARISARITHA
	;CHECK FOR DIVISION "/"
	CMP		AL, 0x2F
	JZ		CHARISARITHA
	;CHECK FOR EQUALS "="
	CMP		AL, 0x3D
	JZ		CHARISARITHA
	;CHECK FOR EXPONENT "^"
	CMP		AL, 0x5E
	JNZ		CHARISARITHB
CHARISARITHA:
	STC
	RET
CHARISARITHB:
	CLC
	RET


