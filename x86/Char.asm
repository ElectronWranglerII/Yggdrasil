;CONVERTS THE ASCII CHARACTER IN AL TO AN INTEGER
;ON ENTRY:
;	AL = ASCII VALUE
;ON RETURN:
;	C = 0 IF CHARACTER IS A NUMBER
;			AL = INTEGER VALUE
;	C = 1 IF CHARACTER IS NOT A NUMBER
;			AL = 0x00
CHARTOINT:
	;CHECK FOR LESS THAN "0"
	SUB		AL, 0x30
	JC		CHARTOINTA
	;CHECK FOR GREATER THAN "9"
	PUSH	AX
	SUB		AL, 0x0A
	POP		AX
	JNC		CHARTOINTA
	CLC
	RET
CHARTOINTA:
	XOR		AL, AL
	STC
	RET


